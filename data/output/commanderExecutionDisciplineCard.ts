- 已明确：统一验收口的四项标准。
- 已明确：从自修、同域换打法、跨面求证、缩目标保交付，到统帅位拍板中止/转派的失败降级链。
- 可直接复用：后续任何任务都可套用“目标—主执行面—验收口—截止—降级层级”模板。
